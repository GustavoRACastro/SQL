-- Limpando tabelas que possam conter o mesmo nome das tabelas a serem criadas.
DROP TABLE Cliente
DROP TABLE Filme
DROP TABLE Locação
DROP TABLE Preço
DROP TABLE Gênero

-- Script para criação das tabelas de trabalho.
CREATE TABLE Cliente (
CodCliente VARCHAR(10) NOT NULL,
Nome VARCHAR(50) NULL,
Endereco VARCHAR(50) NULL,
Bairro VARCHAR (20) NULL,
Cidade VARCHAR (50) NULL,
Estado CHAR(2) NULL
CONSTRAINT pk_cliente PRIMARY KEY (CodCliente)
);

CREATE TABLE Preço (
Cor CHAR(2) NOT NULL,
Valor DECIMAL(10,2) NOT NULL

CONSTRAINT pk_preco PRIMARY KEY (Cor)
);

CREATE TABLE Gênero (
CodGenero VARCHAR(3) NOT NULL,
Descricao VARCHAR(20) NOT NULL

CONSTRAINT pk_genero PRIMARY KEY (CodGenero)
);

CREATE TABLE Filme (
CodFilme VARCHAR(4) NOT NULL,
Nome VARCHAR(50) NOT NULL,
AnoLancamento INT,
CodGenero VARCHAR(3) NOT NULL,
Cor CHAR(2) NOT NULL,
Status CHAR(1) NOT NULL


CONSTRAINT pk_filme PRIMARY KEY (CodFilme),

CONSTRAINT fk_filme_genero FOREIGN KEY (CodGenero)
	REFERENCES Gênero (CodGenero),
CONSTRAINT fk_filme_preco FOREIGN KEY (Cor)
	REFERENCES Preço (Cor)
);

CREATE TABLE Locação (
CodCliente VARCHAR(10) NOT NULL,
DataLocacao DATE NOT NULL,
CodFilme VARCHAR(4) NOT NULL,
DataDevolucao DATE NULL

CONSTRAINT fk_cliente_locacao FOREIGN KEY (CodCliente)
	REFERENCES Cliente(CodCliente),
CONSTRAINT fk_locacao_filme FOREIGN KEY (CodFilme)
	REFERENCES Filme (CodFilme)
);

